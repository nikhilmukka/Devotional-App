function lookupTranslation(tree: TranslationTree | undefined, key: string): string | undefined {
  if (!tree) return undefined;
  const parts = key.split('.');
  let current: string | TranslationTree | undefined = tree;

  for (const part of parts) {
    if (!current || typeof current === 'string') return undefined;
    current = current[part];
  }

  return typeof current === 'string' ? current : undefined;
}

export function t(language: AppLanguage, key: string): string {
  return (
    lookupTranslation(translations[language], key) ||
    lookupTranslation(extendedTranslations[language], key) ||
    lookupTranslation(translations.english, key) ||
    lookupTranslation(extendedTranslations.english, key) ||
    key
  );
}

export function getWeekdayLabel(language: AppLanguage, weekday: number): string {
  const weekdayKeys = [
    'weekdays.sunday',
    'weekdays.monday',
    'weekdays.tuesday',
    'weekdays.wednesday',
    'weekdays.thursday',
    'weekdays.friday',
    'weekdays.saturday',
  ];

  return t(language, weekdayKeys[weekday] || weekdayKeys[0]);
}

export function formatAudioDuration(language: AppLanguage, minutes: number): string {
  return `${minutes} ${t(language, 'audio.minuteUnit')}`;
}

export function getPreferredPrayerLanguages(language: AppLanguage): string[] {
  switch (language) {
    case 'english':
      return ['english', 'hindi', 'sanskrit'];
    case 'hindi':
      return ['hindi', 'sanskrit', 'english'];
    case 'marathi':
      return ['hindi', 'english', 'sanskrit'];
    case 'telugu':
    case 'kannada':
    case 'tamil':
      return ['english', 'hindi', 'sanskrit'];
    default:
      return ['english', 'hindi', 'sanskrit'];
  }
}
