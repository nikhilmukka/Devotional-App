function normalizeLanguage(language: string): LanguageKey {
  return (language || "English").toLowerCase() as LanguageKey;
}

export function t(language: string, path: string) {
  const normalized = normalizeLanguage(language);
  const tree = translations[normalized] ?? translations.english;
  const value = path.split(".").reduce<string | TranslationTree | undefined>((acc, key) => {
    if (typeof acc !== "object" || !acc) return undefined;
    return acc[key];
  }, tree);
  if (typeof value === "string") {
    return value;
  }

  const englishValue = path.split(".").reduce<string | TranslationTree | undefined>((acc, key) => {
    if (typeof acc !== "object" || !acc) return undefined;
    return acc[key];
  }, translations.english);

  return typeof englishValue === "string" ? englishValue : path;
}

export function getLocalizedPrayerTitle(language: string, title: string) {
  const normalized = normalizeLanguage(language);
  if (normalized === "english") return title;
  return prayerTitleTranslations[normalized]?.[title] ?? title;
}

